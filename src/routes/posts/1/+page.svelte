<h1>A Revised New Life <i>[2023-05-03]</i></h1>
<p>
    I have reviewed my previous decision, coming to the conclusion that it is extreme to abolish social media from my life entirely.
    While my initial intention was to dedicate the majority of my downtime to developing projects, a new root issue arised: <b>burnout</b>. 
</p>
<p>
