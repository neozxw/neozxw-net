    My personal observations seem to conclude that I always fall back into the older days, times that should've been left behind.
</p>
<h2>Addressing a Paradox</h2>
<p>
    This results in a self-contradicting situation: I hate social media and want it out of my life, but I keep returning to it.
    In order to fix my dilemna, I will simply change the way I use social media.
</p>
<p>
    Rather than doomscrolling, social media can be implemented to broadcast my hobbies, and meet constructive members of society.
    I'm assuming this is the intent of Web 2.0, it is unfortunate how it devolved into a toxic distraction that I don't want to touch.
</p>
